/*
 * Да се напише програма во која се внесуваат три броеви. Да се отпечати 1 доколку средниот број е
 * истовремено поголем од двата останати броеви, или пак е истовремено помал.
 * Во спротивно да се отпечати 0.
 */

#include<stdio.h>

int main() {
    int a, b, c;
    scanf("%d %d %d", &a, &b, &c);
    if ((b > a && b > c) || (b < a && b < c)) {
        printf("1");
    } else {
        printf("0");
    }
}